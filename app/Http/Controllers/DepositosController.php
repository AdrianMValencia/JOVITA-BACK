    public function store(Request $request){

        try {
            if (!$user = JWTAuth::parseToken()->authenticate()) {
                return response()->json(['user_not_found'], 404);
            }
        } catch (TokenExpiredException $e) {
            return response()->json(['token_expired', 'message' => $e->getMessage()], 401);
        } catch (TokenInvalidException $e) {
            return response()->json(['token_invalid', 'message' => $e->getMessage()], 401);
        } catch (JWTException $e) {
            return response()->json(['token_absent', 'message' => $e->getMessage()], 401);
        }

    $params = $request->all();

    $depositos = new Depositos();
    $depositos->idPuntoVenta = $params['idPuntoVenta'] ?? null;
    $depositos->fechaDeposito = $params['fechaDeposito'] ?? null;
    $depositos->idBanco = $params['idBanco'] ?? null;
    $depositos->imagen = $params['imagen'] ?? null;
    $depositos->observaciones = $params['observaciones'] ?? null;
    $depositos->status = $params['status'] ?? null;
    $depositos->save();

        $data = array(
            'depositos' => $depositos,
            'message' => 'Registro agregado correctamente',
            'status' => 200
        );

        return response()->json($data, $data['status']);
    }

    public function update($id, Request $request){

        try {
            if (!$user = JWTAuth::parseToken()->authenticate()) {
                return response()->json(['user_not_found'], 404);
            }
        } catch (TokenExpiredException $e) {
            return response()->json(['token_expired', 'message' => $e->getMessage()], 401);
        } catch (TokenInvalidException $e) {
            return response()->json(['token_invalid', 'message' => $e->getMessage()], 401);
        } catch (JWTException $e) {
            return response()->json(['token_absent', 'message' => $e->getMessage()], 401);
        }

        $params = $request->all();

        if($depositos = Depositos::find($id)){
            $depositos->idPuntoVenta = $params['idPuntoVenta'] ?? $depositos->idPuntoVenta;
            $depositos->fechaDeposito = $params['fechaDeposito'] ?? $depositos->fechaDeposito;
            $depositos->idBanco = $params['idBanco'] ?? $depositos->idBanco;
            $depositos->imagen = $params['imagen'] ?? $depositos->imagen;
            $depositos->observaciones = $params['observaciones'] ?? $depositos->observaciones;
            $depositos->status = $params['status'] ?? $depositos->status;

            $depositos->save();

            $data = array(
                'depositos' => $depositos,
                'message' => 'Registro actualizado correctamente.',
                'status' => 200
            );

        }else{

            $data = array(
                'message' => 'Codigo no encontrado',
                'status' => 404
            );
        }

        return response()->json($data, $data['status']);
    }

